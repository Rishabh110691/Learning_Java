package Programs;

public class Datatypes {
    public static void main(String[] args) {
        //Integer
        int a=10;
        int b= 15;
        int c=a+b;
        System.out.println(c);

        //Strings
        String str1="Rishabh";
        String str2="Sharma";
        System.out.println(str1+" "+str2);

        //Float
        Float f=10.5f;
        Float g=15.2f;
        System.out.println(f+g);

        //Double
        double d=2132;
        double e=434343;
        System.out.println(d+e);

        //Boolean
        Boolean cond=true;
        Boolean cond2=false;
        System.out.println(cond);

        //char
        char char1='a';
        char char2='b';
        System.out.println(char1+char2);

        //Long
        Long l1=233432342L;
        System.out.println(l1);


    }
}
